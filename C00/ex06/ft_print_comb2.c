#include <unistd.h>

void	ft_putchar(char d, char u)
{
	write (1, &d, 1);
	write (1, &u, 1);
}

void	ft_print_comb2(void)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	while (j <= 98)
	{
		ft_putchar ((j / 10) + 48, (j % 10) + 48);
		write (1, " ", 1);
		ft_putchar ((i / 10) + 48, (i % 10) + 48);
		if (j < 98)
		{
			write (1, ", ", 2);
		}
		i++;
		if (i == 100)
		{
			j++;
			i = j + 1;
		}
	}
}
